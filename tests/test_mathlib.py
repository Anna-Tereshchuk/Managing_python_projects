import pytest
from mathlib import sqrt
  # Імпортуємо функцію sqrt з вашої бібліотеки mathlib

# Тестові випадки для перевірки функції sqrt
# Формат: (вхідне значення, очікуваний результат)
test_cases = [
    (1, 1),            # Квадратний корінь з 1 дорівнює 1
    (2, 1.4142),       # Квадратний корінь з 2 приблизно дорівнює 1.4142
    (4, 2),            # Квадратний корінь з 4 дорівнює 2
    (9, 3),            # Квадратний корінь з 9 дорівнює 3
]

# Використовуємо декоратор для параметризації тестів
@pytest.mark.parametrize("input_value, expected", test_cases)
def test_sqrt(input_value, expected):
    """
    Тестуємо функцію sqrt з різними вхідними значеннями.
    
    Параметри:
    - input_value: число, для якого обчислюємо квадратний корінь.
    - expected: очікуваний результат обчислення.

    Перевіряємо, чи обчислене значення збігається з очікуваним.
    """
    # Обчислюємо результат функції sqrt
    result = sqrt(input_value)
    
    # Перевіряємо, чи результат відповідає очікуваному (з округленням до 4 знаків)
    assert round(result, 4) == expected, f"Failed for input: {input_value}"
